m = int(input("Enter number is "))
n = int(input("Enter number is "))
oper = input("Enter operator is ")

if (oper == "+"):
    print("Summation is", m + n)
    exit()
if (oper == "-"):
    print("Substraction is", m - n)
    exit()
if (oper == "*"):
    print("Multiplication is", m * n)
    exit()
if (oper == "/"):
    print("Division is", m / n)